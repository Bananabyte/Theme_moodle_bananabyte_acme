/**
 * Parses CSS before it is cached.
 *
 * This function can make alterations and replace patterns within the CSS.
 *
 * @param string $css The CSS
 * @param theme_config $theme The theme config object.
 * @return string The parsed CSS The parsed CSS.
 */
function theme_acmebanana_process_css($css, $theme) {

    // Set the background image for the heroimg.
    $heroimg = $theme->setting_file_url('heroimg', 'heroimg');
    $css = theme_acmebanana_set_heroimg($css, $heroimg);



    return $css;

}











/**
 * Adds the logo to CSS.
 *
 * @param string $css The CSS.
 * @param string $logo The URL of the logo.
 * @return string The parsed CSS
 */
function theme_acmebanana_set_heroimg($css, $heroimg) {
    $tag = '[[setting:heroimg]]';
    $replacement = $heroimg;
    if (is_null($replacement)) {
        $replacement = 'pix:theme|fundo';
    }

    $css = str_replace($tag, $replacement, $css);

    return $css;
}







/**
 * Serves any files associated with the theme settings.
 *
 * @param stdClass $course
 * @param stdClass $cm
 * @param context $context
 * @param string $filearea
 * @param array $args
 * @param bool $forcedownload
 * @param array $options
 * @return bool
 */
function theme_acmebanana_pluginfile($course, $cm, $context, $filearea, $args, $forcedownload, array $options = array()) {
    if ($context->contextlevel == CONTEXT_SYSTEM and $filearea === 'heroimg') {
        $theme = theme_config::load('acmebanana');
        // By default, theme files must be cache-able by both browsers and proxies.
        if (!array_key_exists('cacheability', $options)) {
            $options['cacheability'] = 'public';
        }
        return $theme->setting_file_serve('heroimg', $args, $forcedownload, $options);
    } else {
        send_file_not_found();
    }
}


